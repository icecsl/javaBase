    public int uniquePathsWithObstacles(int[][] obstacleGrid) {
        if (obstacleGrid.length == 0) {
            return 0;
        }
        return step(obstacleGrid,0,0);
    }

    private int step(int[][] obstacleGrid, int i, int j) {
        if (i>=obstacleGrid.length||j>=obstacleGrid[i].length) {
            return 0;
        }

        int num = obstacleGrid[i][j];
        if (num == 1) {
            return 0;
        }
        if (num>1) {
            return num-2;
        }
        if (i==obstacleGrid.length-1 && j==obstacleGrid[i].length-1) {
            return 1;
        }

        int down = step(obstacleGrid, i+1, j);
        int right = step(obstacleGrid, i, j+1);
        obstacleGrid[i][j] = down+right+2;
        return down+right;
    }
